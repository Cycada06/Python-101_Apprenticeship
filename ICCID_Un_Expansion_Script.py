        "import csv\n",
        "from google.colab import files\n",
        "\n",
        "# Function to read numbers from a single column CSV file with a header\n",
        "def read_numbers_from_csv(filename):\n",
        "    numbers = []\n",
        "    with open(filename, 'r') as file:\n",
        "        reader = csv.reader(file)\n",
        "        next(reader)  # Skip the header row\n",
        "        for row in reader:\n",
        "            try:\n",
        "                num = int(row[0].strip())\n",
        "                numbers.append(num)\n",
        "            except ValueError:\n",
        "                print(f\"Skipping non-integer value: {row[0]}\")\n",
        "    return numbers\n",
        "\n",
        "# Function to group numbers into batches of size batch_size\n",
        "def group_into_batches(numbers, batch_size):\n",
        "    batches = []\n",
        "    for i in range(0, len(numbers), batch_size):\n",
        "        batch = numbers[i:i + batch_size]\n",
        "        batches.append(batch)\n",
        "    return batches\n",
        "\n",
        "# Function to process each batch and find the first, last numbers and count\n",
        "def process_batches(batches):\n",
        "    results = []\n",
        "    for batch in batches:\n",
        "        if batch:\n",
        "            first_number = batch[0]\n",
        "            last_number = batch[-1]\n",
        "            count = len(batch)\n",
        "            results.append((first_number, last_number, count))\n",
        "    return results\n",
        "\n",
